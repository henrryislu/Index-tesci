<h4>Transporte por carretera</h4>
<p>El transporte terrestre es la alternativa más común para transportar mercancías, sobre todo a distancias cortas, en los países desarrollados. En ellos, la existencia de una amplia red de infraestructuras y carreteras permite servicios como la recogida y entrega puerta a puerta. Incluso en el caso del transporte por avión, tren o barco, la entrega final suele ser por carretera, ya sea en camión o en furgoneta.</p>
<br>
<p>Las principales ventajas del transporte por carretera son que es económico, rápido, y se adapta a casi cualquier tipo de mercancía. La carretera ofrece una gran flexibilidad horaria, aunque su principal inconveniente es precisamente que su velocidad depende de las condiciones del tráfico.</p><br>
<ul>
<li><strong>Rabón (1 eje) y Torton (2 ejes):</strong>: este tipo de unidades se usa para cargas pequeñas, por lo general mudanzas, carga seca y paquetería. La capacidad es de 8 ton / Medidas: 6.50 x 2.50 x 2.40 m y 15 ton / Medidas: 6.50 x 2.50 x 2.40 m respectivamente.</li>
<li><strong>Caja seca (cerrada):</strong>se traslada carga seca, la caja de 53 pies tiene capacidad hasta de 25 ton y sus medidas son: 15.90 x 2.50 x 2.50 m, la caja de 48 pies cuenta con la misma capacidad y sus medidas son: 14.50 x 2.50 x 2.50 m.</li>
<li><strong>Doble remolque:</strong>este tipo de articulación también es llamado full, está diseñado para transportar carga contenerizada y carga suelta. La capacidad máxima es de 50 ton.</li>
<li><strong>Caja refrigerada:</strong>es destinado para el traslado de carga que debe estar bajo condiciones especiales de temperatura. Está indicado para productos perecederos y la mercancía con sensibilidad a temperaturas.</li>
<li><strong>Plataforma:</strong>se utiliza para el transporte de contenedores y material pesado a granel. La capacidad es de hasta 25 ton. El uso de contenedores facilita el traslado de la mercancía en el transporte multimodal.</li>
<li><strong>Autotanque:</strong>también es llamado pipa, es el idóneo para carga de líquidos, productos químicos, petroquímicos y gases. Puede contener un máximo de 25,000 litros.</li>
<li><strong>Low boy / cama baja:</strong>este tipo de remolque es utilizado para el transporte de maquinaria o productos pesados y/o con sobredimensión. Dependiendo del tipo de remolque puede llegar a tener una capacidad de carga de hasta 80 ton.</li>
<li><strong>Tolva:</strong>está diseñado para la carga a granel y para el traslado de material para construcción</li>
<li><strong>Madrina:</strong>esta versión es una porta vehículos, tiene capacidad para transportar hasta 21 vehículos y la capacidad es de 35 ton.</li>
<li><strong>Camioneta de 3 y 1/2:</strong>una solución de carga de material anterior a un remolque completo. Capacidad de carga de 3.5 toneladas, prácticamente cualquier necesidad de transporte de material a mediana escala puede resolverse con este vehículo.</li>
<li><strong>Camioneta Pick-up o mini-truck:</strong>son las camionetas más versátiles en cuanto a posibilidades de carga y transporte. Tienen una amplia variedad de modelos y tipos de caja que pueden adaptarse a diferentes situaciones. Son ideales para transporte corto y constante de material o para obras de construcción menores. Por ejemplo, para pedidos de material de un centro de distribución a un punto de venta.</li>
</ul>
